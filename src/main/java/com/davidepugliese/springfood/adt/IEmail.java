package com.davidepugliese.springfood.adt;
import com.sun.javaws.exceptions.InvalidArgumentException;

import java.util.regex.Pattern;

public interface IEmail {
   static String create(String email) throws InvalidArgumentException {
      if (validate(email)) {
          Email theEmail = new Email(email);
          return theEmail.email;
      } else {
         throw new InvalidArgumentException(new String[]{"Not valid email"});
      }
   }
   static boolean validate(String email) {
      Pattern ptr = Pattern.compile("(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])");
      return ptr.matcher(String.valueOf(email)).matches();
   }
}
